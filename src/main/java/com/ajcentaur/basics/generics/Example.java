package com.ajcentaur.basics.generics;

public class Example<Z,X,C> {
    private Z a;

    private X b;

    private C c;

    public Z getA() {
        return a;
    }

    public void setA(Z a) {
        this.a = a;
    }

    public X getB() {
        return b;
    }

    public void setB(X b) {
        this.b = b;
    }

    public C getC() {
        return c;
    }

    public void setC(C c) {
        this.c = c;
    }
}
